/**
 * 题目描述:在一个二维数组中（每个一维数组的长度相同），每一行都按照从左到右递增的顺序排序，
 * 每一列都按照从上到下递增的顺序排序。请完成一个函数，输入这样的一个二维数组和一个整数，
 * 判断数组中是否含有该整数。
 * @param {Number} target 
 * @param {Array} array 
 */
function Find(target, array) {
  let ly = array.length;
  let lx = array[0].length;
  if (target < array[0][0] || target > array[ly - 1][lx - 1]) return false;
  for (let i = ly - 1, j = 0; i >= 0 && j <= lx - 1;) {
    if (array[i][j] < target) j++;
    else if (array[i][j] > target) i--;
    else {
      return true;
    }
  }
  return false;
}
